def redundante():
    condicao = None
    if condicao is None:
        return False
    else:
        return True

def naoredundante():
    condicao = None
    if condicao is None:
        return False
    return True

def inteligente():
    condicao = None
    return not condicao is None

print(redundante())
print(naoredundante())
print(inteligente())
