# Tupla é uma Lista imutável. O que diferencia a Estrutura de Dados Lista da Estrutura de Dados Tupla é que a primeira pode
# ter elementos adicionados a qualquer momento, enquanto que a segunda estrutura, após definida, não permite a adição ou remoção
# de elementos.

t=(1,2,3)               #define uma tupla
print(t.index(1))    #retorna o indice do elemento 1
print(t.count(1))    #retorna quantas vezes apareceu o elemento 1"